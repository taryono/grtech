    <script>
        $(function() { 
            $(window).on("load", function() {
                $.each($("img.image-logo"), function(){ 
                     $(this).attr("onerror", "this.src='{{(asset('assets/images/company.png'))}}'");
                 });
            });

            $("#employee").DataTable({
                "responsive": true,
                "lengthChange": false, 
                "processing": true,
                "serverSide": true,
                "ajax": "{{route('employee.index')}}",
                "columns": [
                    // data:column name from server ,name:alias
                    {data: 'id', name: 'id'},
                    {data: 'full_name', name: 'full_name'},
                    {data: 'email', name: 'email'},
                    {data: 'phone', name: 'phone'},
                    {data: 'company', name: 'company'}, 
                    {data: 'action', name: 'action', orderable: false, searchable: false},
                ]
            });

            $("#company").DataTable({
                "responsive": true,
                "lengthChange": false, 
                "processing": true,
                "serverSide": true,
                "ajax": "{{route('company.index')}}",
                "columns": [
                    {data: 'id', name: 'id'},
                    {data: 'name', name: 'name'},
                    {data: 'email', name: 'email'},
                    {data: 'logo', name: 'logo'},
                    {data: 'website', name: 'website'},
                    {data: 'action', name: 'action', orderable: false, searchable: false},
                ]
            });

            $("body").on('click','a.detail,a.edit', function(e){
                e.preventDefault();
                $target = $(this).attr('data-target');
                $($target+" h4.modal-title").text($(this).attr('data-title'));
                $.ajax({
                    url:$(this).attr('href'),
                    type:"GET",
                    success:function(res){
                        $($target+" div.modal-body").html(res);
                    },
                    error:function(res){
                        message('error',res.responseText);
                    }
                });
            }).on('click','a.delete', function(e){
                e.preventDefault(); 
                if(confirm('Are You Sure?')){
                    $row = $(this).parents().closest('tr').remove();
                    $.ajax({
                        url:$(this).attr('href'),
                        type:"DELETE", 
                        dataType: 'JSON',
                        data:{ 
                            '_token': '{{ csrf_token() }}',
                        },
                        success:function(res){
                            message(res.status, res.message);
                        },
                        error:function(res){
                            message('error', res.responseText);
                        }
                    });
                }
            }).on('click','button.submit-form', function(e){
                e.preventDefault(); 
                $.ajax({
                        url:$(this).parent().closest('form').attr('action'),
                        type:"PUT",  
                        data: $(this).parent().closest('form').serialize(),
                        success:function(res){
                            message(res.status, res.message);
                            $("button.close").click();
                        },
                        error:function(res){
                            message('error', res.responseText);
                        }
                    });
            });
        });
    </script>
